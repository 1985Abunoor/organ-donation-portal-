        <p>Organ donation is the process of surgically removing an organ or tissue from one person (the donor) and transplanting it into another person (the recipient). Organ donation can save lives and improve the quality of life for people with organ failure or certain medical conditions.</p>

        <div class="icon-box">
          <div class="icon"><i class="a-sharp fa fa-check"></i></div>
          <h4 class="title"><a href="">Quality of life</a></h4>
          <p class="description">Organ donation can significantly improve the quality of life for recipients, often allowing them to live longer and more fulfilling lives. However, there are challenges and ethical considerations surrounding organ donation, including ensuring fair and equitable allocation of organs, addressing concerns about consent, and managing the risks associated with surgery and immunosuppressive medications.</p>
        </div>

        <div class="icon-box">
          <div class="icon"><i class="a-sharp fa fa-check"></i></div>
          <h4 class="title"><a href="">Deceased Donation</a></h4>
          <p class="description">Organs are donated from individuals who have died, typically from accidents, strokes, heart attacks, or other causes of brain death. Organs such as the heart, lungs, liver, kidneys, pancreas, and intestines can be donated after death. Tissues like corneas, skin, bone, and heart valves can also be donated after death</p>
        </div>

        <div class="icon-box">
          <div class="icon"><i class="a-sharp fa fa-check"></i></div>
          <h4 class="title"><a href="">Living Donation</a></h4>
          <p class="description">In this type of donation, a living person donates a kidney, a portion of the liver, lung, intestine, or pancreas to another person in need. Living donation usually involves family members or close friends of the recipient, but it can also involve altruistic donors who donate to strangers.</p>
